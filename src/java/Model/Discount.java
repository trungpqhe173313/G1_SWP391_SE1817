package Model;

/**
 *
 * @author phamt
 */
public class Discount {
    private int id;
    private int point;
    private int discount;

    public Discount() {
    }

    public Discount(int id, int point, int discount) {
        this.id = id;
        this.point = point;
        this.discount = discount;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getPoint() {
        return point;
    }

    public void setPoint(int point) {
        this.point = point;
    }

    public int getDiscount() {
        return discount;
    }

    public void setDiscount(int discount) {
        this.discount = discount;
    }
    
    
}
