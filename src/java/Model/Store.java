package Model;

/**
 *
 * @author phamt
 */
public class Store {
    private int id;
    private boolean isActive;

    public Store() {
    }

    public Store(int id, boolean isActive) {
        this.id = id;
        this.isActive = isActive;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public boolean isIsActive() {
        return isActive;
    }

    public void setIsActive(boolean isActive) {
        this.isActive = isActive;
    }
    
    
}
